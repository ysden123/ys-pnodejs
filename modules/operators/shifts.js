// Playing with shift operators
'use strict';

console.log(' 1 >> 1 = %d', 1 >> 1);
console.log(' 3 >> 1 = %d', 3 >> 1);
console.log(' 3 >> 2 = %d', 3 >> 2);
console.log(' 10 >> 1 = %d', 10 >> 1);
console.log(' 10 >> 2 = %d', 10 >> 2);

console.log(' 1 >>> 1 = %d', 1 >>> 1);
console.log(' 3 >>> 1 = %d', 3 >>> 1);
console.log(' 3 >>> 2 = %d', 3 >>> 2);
console.log(' 10 >>> 1 = %d', 10 >>> 1);
console.log(' 10 >>> 2 = %d', 10 >>> 2);